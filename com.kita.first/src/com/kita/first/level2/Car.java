package com.kita.first.level2;

public class Car {
	private int speed;
	private String color;
	
	Car(int speed) {
		this.speed = speed;
	}
	
	void setSpeed(int speed) {
		this.speed = speed;
	}
	
	int getSpeed() {
		return speed;
	}
	
	void setColor(String color) {
		this.color = color;
	}
	
	String getColor() {
		return color;
	}
}



